//Area of various shapes

//Rectangle area calculate 
//Rectangle formula -> Base * Height
var base = parseFloat(prompt("Enter base :"));
var height = parseFloat(prompt("Enter height :"));

var area = base * height;//Rectangle formula
alert("Area : " + area);


//Triangle area calculate 
//Triangle formula -> Base * Height / 2
var base = parseFloat(prompt("Enter base :"));
var height = parseFloat(prompt("Enter height :"));

var area = (base * height) / 2;//Triangle formula
alert("Area : " + area);

